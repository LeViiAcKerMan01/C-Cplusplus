#include<iostream>
using namespace std;

int main()
{
    int index=6;
    do{
        cout<<index<<endl;
        index++;

    }
    while(index<=5);
    return 0;
}
/* 
do while loop :
 
first executes the block of code and then look over the condition and if the condition holds true only then it will execute the block of code further else it stops!

*/